<a name="Tournaments"></a><h3><img src="images/icons/platinum.png" alt=" " /> Special Rules Games / Tournaments:</h3>
<p>
	There are two types of special Games on the site: Special Rules Games and Tournaments. If you're interested in starting or running either type please open a thread in the <a href="modforum.php">ModForum</a> and explain your idea to the moderator team in order to get approval. 
	This approval is necessary to ensure that the game(s) are set up properly, avoiding potential issues that can occur when a game is not properly planned. <br /><br />
	Once you get our approval, you can start a sign-up thread on the Forum posting the approved Rulebook (or a link to it) and the name of the Director (usually who opened the Sign-up thread) in the opening post or immediately after. In the sign-up thread interested players should be able to read the Director's name and the moderator approved rules before deciding whether to join. Sign-up threads without moderator approved Rules will be silenced.
	Special Games must be passworded.
</p>

<h4>1. Special Rules Games:</h4>
<p>
	An Special Rules Games is a game which uses a different rule set to standard games. Notable examples can be found on the vDip Wiki, including Bourse Diplomacy, Shift Diplomacy, Treaties and Contract Gunboat. If you want to run a Special Rules Games, please post in the modforum explaining your idea - the moderators will ask you some questions you’ll need to answer before posting the idea to the forum, to make sure that the rules are clear. The answers to these question will need to be included in the forum thread advertising the game to ensure all members fully understand the games rules and objectives. 
</p>

<h4>2. Tournaments:</h4>
<p>
	These are larger series of games with unique scoring rules to determine a winner. These games will have a Tournament Director (TD), who will oversee all aspects of the tournament. This person will be responsible for making decisions relating to the scoring, pauses, and anything affecting game play.
	<ul style="margin-top: -20px;" >
		<li>Must request approval *before* advertising. The moderator team reserves the right to deny tournaments or tournament directors.</li>
		<li>Must include a set of rules for potential participants to agree upon, that all participants must agree too.</li>
		<li>In the case of cheating less severe than multi-accounting the Tournament Director will be informed and given the choice of removing this player from the tournament. Participants agree to honor the TD's decision in these matters and send all complaints privately to the TD or moderator team.</li>
		<li>The moderator team will enforce site-wide rules in tournaments as they do other games.</li>
		<li>Players within tournaments have to accept that the TD moderates their own tournament decisions. Please choose which tournaments to join carefully!</li>
		<li>TD's may request the list and position of players in their tournament even in anonymous games except for games they are participating in.</li>
		<li>TD's may request the moderator team pm or email any player with questions or warnings regarding the tournament.</li>
		<li>TD's may request a message be posted in every game’s global message window, including anon or gunboat games, to remind participants of tournament rules or for tournament scoring updates.</li>
		<li>If a TD is given control of country assignments, force pause, draw, and so forth, he/she cannot moderate their own game. Either a second designated TD or a mod may moderate the game instead, but the TD will still have final say on any decisions.</li>
		<li>As with all mod / admin decisions if you disagree with a decision you can appeal it. See below for the mod / admin contact details.</li>
	</ul>
	</p><p style="margin-top: -20px;">
	Please open a thread on the mod forum if you want to start a tournament (see the contact details below), and you will be asked a list of questions that will help us set the tournament up. The answers to the questions asked by the mod team will need to be included in the thread advertising for the tournament. Threads advertising a tournament without approval and without these answers will be locked.
