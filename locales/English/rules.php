<a name="ModPolicies"></a><h3><img src="images/icons/mod.png" alt=" " /> Mod Policies:</h3>

<h4>Cancelling games</h4>
<p>
	The moderator team will consider cancelling games when there are 2 or more cheaters in a game and/or they had a considerable impact on the game as a whole. We will encourage the remaining players to cancel on their own. If they do not, we will ask for an explanation. We will determine if the explanation is warranted and either leave it alone or force cancel with an explanation. If no players have been eliminated the remaining players may choose to draw the game before it is cancelled. The number of players caught cheating and banned before the Mod/Admin team decides to cancel a game may go up or down depending on the variant type, and how many members are involved in the game.<br /><br />
	Games that have been paused for over 2 months without any activity will be cancelled. 
	In games paused for over 2 months with some activity we will post in global asking if there are objections to a cancellation. 
</p>

<h4>Pausing/Unpausing</h4>
<p>
	The players should take care of handling delays to the game themselves as much as they can. If they have agreed to pause, then they should also have agreed when to unpause. If the unpause doesn't happen by said date, then they can report the issue to the <a href="modforum.php">ModForum</a> and we will look into it. If you are unable to keep up with a game please consider using a <a href="howto.php?howto=CountrySwap">game-sitter</a>. We will not consider pause/extend requests if the requester hasn't already entered their Extend vote in their game(s).
</p>

<h4>Moderator Responsibilities and Appealing decisions</h4>
<p>
	As users have their own set of responsibilities, so does the moderator staff.
	As representatives of the vDiplomacy users, the moderators will be held accountable for disciplinary decisions.
	While judgment regarding forum rule violations remains at the sole discretion of the moderator staff,
	users will be provided with a clear warning regarding rules violations in either a forum post or private message. <br />
	Users can appeal any moderator's decision by opening a thread on the <a href="modforum.php">ModForum</a>
	(or, if your account has been banned, by sending an email to <a href="mailto:<?php print (isset(Config::$modEMail) ? Config::$modEMail : Config::$adminEMail); ?>">
	<?php print (isset(Config::$modEMail) ? Config::$modEMail : Config::$adminEMail); ?></a>) specifying the grounds for the appeal. 
	The appeal will be dealt with by one of the admins who was not involved in the original decision or by the site owner. 
	Appeals made on behalf of other members will not be considered. The outcome of an appeal is final.
	<br /><br />
	There may be a formal explanation and discussion on moderators' decisions in the ModForum, but the moderator staff will never disclose information that may contain personal information about members or about the methods used to enforce the rules.
</p>

