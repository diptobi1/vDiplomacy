<a name="silenceInfo"></a><h3><img src="images/icons/unmute.png" alt=" " /> Forum Moderation:</h3>

<h4>Self-moderation:</h4>
<p>
	A user-driven approach is the primary method of forum moderation;
	<ul style="margin-top: -20px;" >
		<li>You can mute any other user that you don't want to see in the forum or get PMs from.</li>
		<li>You can mute any forum thread you aren't interested in.</li>
		<li>You can mute any country playing in a game with you.</li>
		<li>You can <a href="features.php?#2_3">block a player</a> if you do not want them to join your games. This feature works both ways, you cannot join games with the player you blocked, so don't overuse this feature or your capability to join new games might result drastically reduced. Check on your Settings page to see your list of blocked players.<br>	This allows you to block out anyone or any discussion you find offensive, and be as open or restrictive in what you see and play as you like!</li>
	</ul>	
</p>

<h4>Site-moderation:</h4>
<p>
	In order to maintain a positive user experience please follow the forum rules below!
	<ul style="margin-top: -20px;" >
		<li>Do not make personal threats toward members or their families. This is a Diplomacy site and no members should have to worry about actions taken against them in real life.</li>
		<li>Do not post or make reference to personal information about another member without their specific consent.</li>
		<li>Do not make targeted threads that are abusive or degrading toward another member, group of members, or social group. This includes but is not limited to posts regarding sexuality, race, and religion.</li>
		<li>Creating an account for the sole purpose of advertising goods or services is not allowed, but a member can post an advertisement as long as he/she does not spam the forum by posting the same message repeatedly.</li>
		<li>Punishment for infractions may range from a warning, or a temporary silence/freeze of at least 48 hours, to a ban. Silences will prevent forum posts as well as private messages. Bans are an instrument of last resort in moderating Forums and the moderators will seek to avoid issuing them where reasonably possible.</li>
	</ul>	
	</p><p style="margin-top: -20px;" >
	Additionally, threads which share information on an ongoing game have a negative impact on games which self-moderation can't prevent, so the mod team can lock/silence these threads:
	<ul style="margin-top: -20px;" >
		<li>Silences can apply to a user or a thread.</li>
		<li>Thread silences last indefinitely, but user silences expire after a brief cool-off period.</li>
		<li>Silenced threads / users can still be viewed, however no more messages may be posted.</li>
		<li>Silenced users can still play in games; a silence only affects the forum and pm's.</li>
	</ul>
	</p><p style="margin-top: -20px;" >
	Do not create new accounts to work around user silences, or post threads to continue on from ones which have been silenced! Do not post messages on the forum for users who have been silenced. This may result in bans etc, which would be worse than simply keeping off the forum for a few days.
	When in doubt about whether posting something is allowed, remember your first responsibility: "use common sense and respect other players!"
</p>

