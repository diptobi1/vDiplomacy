<h4>What to send:</h4>
<p>
	Always try to have some clear reasons when contacting and do not mention it on the forum. In your post you should include:
	<ul style="margin-top: -20px;" >
		<li>If making a general request (e.g for an unpause):</li>
		<li>Ensure that you make the subject line useful (e.g. "Unpausing help" or "Phase-length change") to help us solve the matter as quickly as possible.</li>
		<li>If reporting a cheating issue:
			<ul style="margin-left:0px;">
				<li>Links to the accounts of the players who you suspect.</li>
				<li>Link(s) to any specific game(s) that raised your suspicions.</li>
				<li>Any relevant information that you have noticed. Please make clear who and what actions have raised your suspicions. Vague reports make investigations difficult, and may result in us missing whatever made you suspicious. </li>
			</ul>	
		</li>
	</ul>	
	</p><p style="margin-top: -20px;">
	We will try to reply to you quickly, but be patient: investigations can take quite some time. If we have not followed up with you in 72 hours please feel free to follow up with us. 
</p>

