<a name="Rules"></a><h3><img src="images/icons/alert.png" alt=" " /> Rules:</h3>

<a name="MultiAccounting"></a><h4>1. No Multi-Accounting</h4>
<p>
	You may only have <em>one account</em>, second accounts are not allowed under <em>any circumstances</em>, and will be banned. This may also lead to your first account also being banned.&nbsp; If you forget your password, use the lost password finder <a href="logon.php?forgotPassword=1">here</a>. If you are still unable to log in, contact the mods.
</p>

<a name="MetaGaming"></a><h4>2. No Meta-gaming</h4>
<p>
	Metagaming is when you make a decision in a game for reasons outside the game.
	For example, you cannot make alliances because you are friends/relatives or in return for a favor in another game (cross-game alliances), because such an alliance would give an unfair advantage to those involved. Metagaming is unfair to all the other members involved with the games, so these offenses are taken very seriously.
	<ul style="margin-top: -20px;" >
		<li><b>Using Public Site Information:</b><br />
			Metagaming is using information from outside of the game to impact in game decisions. There are limited exceptions to this prohibition if everyone in the game has the same access to the information. For example, looking at another player's game records in a public game is okay because everyone in the game has completely equal access to those records. <br /><br />
		</li>
		<li><b>Forbidden Metagaming:</b><br />
			Playing in a public (non-passworded) game with someone you know from outside of the site is not allowed because your relationship with that person gives you unequal access to information that can impact the game. This is unfair to the other players you and your friend(s) would be playing against. <br /><br />
			An easy solution for groups of friends (or relatives, co-workers, schoolmates, etc.) who want to play together is to create and join a Private (passworded) game. Remember to report your out of site relationship to the mod team so you are not flagged for playing unfairly. The creator of the game can give the password to the members of the group to make sure no one else joins your game by mistake. This is very effective in preventing misunderstandings and metagaming accusations. <br /><br />
			If your group of friends is not large enough to play a game together you are welcome to invite other members into your game by advertising the game on the forum. Just make sure to explain that some people in the game know each other outside the site in your advertisement. <br /><br />
		</li>
		<li><b>Forbidden In-Site Metagaming:</b><br />
		You may not attack or ally with someone because of a specific relationship with that person (example: you are friends or enemies on the forum or in general on the site). Bringing in-site friendships or animosities into a game is not fair to the other people in the game. <br /><br />
		If you are worried about this type of metagaming it is recommended that you join anonymous games. 
		</li>
	</ul>
</p>

<a name="Press"></a><h4>3. Do not abuse game related communication.</h4>
<p>
	<ul style="margin-top: -20px;" >
		<li><b>Do not work around in-game messaging rules: Full Press - Public Press - No Press (Gunboat):</b><br />
			If you enter a game with limited press/messaging (such as global-only) then you can't use other methods such as the forum, email, or PM to bypass this. <br /><br />
			If you are playing in a gunboat (no press/anon) game, do not post anything about your game in the forum and refrain from posting in any threads you feel might be referencing your game. <br /><br />
			Bypassing limited press game rules by communicating with other players in any other way outside the game, such as talking on the Forums, via PM, via e-mail, via phone or in person, is against the rules and spirit of Online-Diplomacy because it gives an unfair advantage to those involved. <br /><br />
			A game starts when its first phase has started (you get an automated message on your Homepage's Notes). Negotiations, even on global chat, cannot start until you get this message. <br /><br />
		</li>
		<li><b>Do not talk about ongoing games on the forum:</b><br />
			Talking about finished games on the forum to discuss strategies, tactics and skills or qualities of variants is allowed and encouraged.<br /><br />
			Talking about an ongoing game - whether you're in it or not - is not allowed on the Forum as it could alter the game. There are no exceptions to this rule. <br /><br />
			Posting anything in the forum about an ongoing gunboat (no press) game or a limited press game is not allowed. This includes offering advice to game participants or predicting the outcome of a game. All questions about a gunboat game must wait until the game is completed.<br /><br />
			If you're having any issue with an ongoing game of yours, please report it in the <a href="modforum.php">ModForum</a>.<br /><br />
			Promoting just created (not yet started) games is allowed. There are dedicated threads (<a href="forum.php?threadID=28853">NON-Live Games</a> or <a href="forum.php?threadID=46">Live Games</a>) to assist you with promoting your game, but make sure you are following Rule #3.<br /><br />
			Searching for game-sitters is not an exception to the rule. Do not ever mention the games you are in on the Forum, just say you are searching for a game-sitter. You can discuss details with a volunteer later, via PM, if need be.<br /><br />
		</li>
		<li><b>Watch what you type: preserve anonymity:</b><br />
			Do not discuss anonymous games you are in or intend on joining.  You are never allowed to reveal your identity, whether inside or outside the game. If you are questioned on your identity in an anonymous game please contact the moderator team by reporting on the <a href="modforum.php">ModForum</a>. 
			Promoting just created Anonymous games or searching for a replacement for a CD is allowed if it is <a href="howto.php?howto=AnonPost">done anonymously</a> in an appropriate thread in the Forum. 
			Anonymous posts on the Forums are allowed only to:
			<ul style="margin-left:0px;">
				<li>advertise new games</li>
				<li>advertise that there are CD-Countries in your anon game someone could take over</li>
			</ul><br />
		</li>
		<li><b>Special Games communication exceptions:</b><br />
			Special Games must be approved by the Mod/Admin Team. See below the <a href="rules.php?#Tournaments">Special Games</a> section for details.
			Provided the terms of the special game are clear to the all players involved before they join the mod team will allow whatever type of communication all players agree upon. Those terms must be written or linked into a Forum thread, so that anyone involved can check them anytime.
		</li>
	</ul>
</p>

<a name="Press Rules"></a><h4>4. Keep press appropriate.</h4>
<p>
Diplomacy is a game of many strategies and your discussions with other players can sometimes be hostile, impolite, and even uncivilized. These press strategies are all permissible on webDiplomacy and in Diplomacy in general. However, your press with other players should never include the following:
</br></br>
-Messages of sexual nature</br>
-Overt racism or sexism of any kind</br>
-Doxxing (revealing any real life information) of another player</br>
</br>
This rule also applies to game titles.
</br></br>
First offenses will result in a warning in some circumstances but may result in a severe point dock and removal from the game in question if the offense necessitates such action. Second offenses may result in a temporary or permanent ban from webDiplomacy.
</p>

<a name="Mods"></a><h4>5. Do not use the software or moderators as a diplomacy tactic</h4>
<p>
  Lying is fine in Diplomacy except when it gives mods/admins unnecessary work to do!<br /><br />
  Falsely accusing someone of being a multi-accounter/meta-gaming to rally others against them in a game, or claiming that your orders didn't
  come out the way you entered them to cover up your motives, means mods/admins spend time looking for cheaters/bugs which were made up.<br /><br />

  All accusations against other players must be made directly to the mod/admin team directly as specified below, in private, for this reason, and software bug claims
  must be genuine, even if only told to another player. Avoid making cheating accusations in game, via pm, or on the forum. 
</p>

